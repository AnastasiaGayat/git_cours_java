public class Serie
{

    int chiffre;
    int length;


    public Serie() {

        chiffre = 0;
        length = 0;
    }


    public int getChiffre() {
        return chiffre;
    }

    public void setChiffre(int chiffre) {
        this.chiffre = chiffre;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }
}
